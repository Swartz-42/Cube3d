#include "../include/libft.h"

int		ft_strchr(const char *s, int c)
{
	int i;

	i = -1;
	if (!s)
		return (0);
	while (s[++i])
	{
		if (s[i] == c)
			return (i + 1);
	}
	return (0);
}
